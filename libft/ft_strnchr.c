#include "libft.h"

int	ft_strnchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (*s)
	{
		if (*s == (char)c)
			i++;
		s++;
	}
	if ((char)c == '\0')
		return (1);
	return (i);
}
